SELECT nombre_zapato, ritmo_km, sexo, precio, link_imagen, descripcion
FROM zapato
WHERE precio < 500000;


--b
SELECT nombre_zapato,
	CASE ritmo_km
    WHEN "3:45" THEN "Zapato para ritmos muy rápidos"
    WHEN "4:00" THEN "Zapato para ritmos rápidos"
    WHEN "4:15" THEN "Zapato para ritmos medios"
    ELSE "Zapato para diversidad de ritmos"
    END AS "Tipos de ritmo"
FROM zapato;


--c
SET lc_times_names = 'es_ES';
SELECT DATE_FORMAT(fecha_compra, '%d - %b - %Y') AS fecha_compra
FROM factura;


--d
